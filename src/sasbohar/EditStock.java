    public boolean validation() {

        if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Product ID can not be empty", "Warning", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (jTextField2.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Size can not be empty", "Warning", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            return true;
        }

    }

