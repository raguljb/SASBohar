    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        boolean validate = validation();
        if (validate) {
            Connection conn;
            String pid = jTextField1.getText();
            String stock = jTextField3.getText();
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
            LocalDateTime now = LocalDateTime.now();
            String lastupdate = dtf.format(now);
            try {
                conn = DriverManager.getConnection("jdbc:mysql://localhost/sasbohar", "sasuser", "sasuser");
                Statement stmt = conn.createStatement();
                stmt.execute("UPDATE stock SET stock='" + stock + "',last_update='" + lastupdate + "' WHERE p_id='" + pid + "';");
                JOptionPane.showMessageDialog(null, "Changes Successfully Saved", "Success", JOptionPane.INFORMATION_MESSAGE);
                conn.close();
                this.dispose();
                // Do something with the Connection
            } catch (SQLException ex) {
                // handle any errors

                MainMenu.stbarred("SQLException: " + ex.getMessage());
                MainMenu.stbarred("SQLState: " + ex.getSQLState());
                MainMenu.stbarred("VendorError: " + ex.getErrorCode());
            }
        }
        Stock.refresh();
    }//GEN-LAST:event_saveActionPerformed

